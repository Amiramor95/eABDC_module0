        http_response_code(400);
        return response([
            'message' => 'Data validation error.',
            'errorCode' => 4106
        ],400);
    }
        try {

            $data = new TrMode;
            $data->TR_MODE_NAME = strtoupper($request->TR_MODE_NAME);
