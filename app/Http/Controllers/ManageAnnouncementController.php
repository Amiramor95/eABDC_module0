            ->join('MANAGE_EVENT', 'MANAGE_EVENT.MANAGE_ANNOUNCEMENT_ID', '=', 'MANAGE_ANNOUNCEMENT.MANAGE_ANNOUNCEMENT_ID')
            ->join('MANAGE_EVENT_DOCUMENT', 'MANAGE_EVENT_DOCUMENT.MANAGE_EVENT_ID', '=', 'MANAGE_EVENT.MANAGE_EVENT_ID')
            ->get();

