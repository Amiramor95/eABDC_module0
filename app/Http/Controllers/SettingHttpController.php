                'message' => ''
            ]);

        } catch (RequestException $r) {

            http_response_code(400);
            return response([
                'message' => '',
                'errorCode' => 4104
            ],400);
        }
    }

    public function update(Request $request)
    {
        $validator = Validator::make($request->all(), [
			'API_KEY' => 'required',
			'API_SECRET' => 'required',
			'ALLOW_IP' => 'required',
        ]);

        if ($validator->fails()) {
            http_response_code(400);
            return response([
                'message' => 'Data validation error.',
                'errorCode' => 4106
            ],400);
        }

        try {
            $data = SettingHttp::where('SETTING_HTTP_ID',$request->SETTING_HTTP_ID)->first();
            $data->USER_NAME = $request->USER_NAME;
            $data->API_KEY = $request->API_KEY;
            $data->API_SECRET = $request->API_SECRET;
            $data->ALLOW_IP = $request->ALLOW_IP;
            $data->DLR_URL = $request->DLR_URL;
            $data->MO_URL  = $request->MO_URL;
            $data->save();
            http_response_code(200);
            return response([
                'message' => 'Data successfully updated.'
            ]);

        } catch (RequestException $r) {

            http_response_code(400);
            return response([
                'message' => 'Data failed to be updated.',
                'errorCode' => 4101
            ],400);
        }
    }

    public function delete($id)
    {
        try {
            $data = SettingHttp::find($id);
            $data->delete();

            http_response_code(200);
            return response([
                'message' => 'Data successfully deleted.'
            ]);

        } catch (RequestException $r) {

            http_response_code(400);
            return response([
                'message' => 'Data failed to be deleted.',
                'errorCode' => 4102
            ],400);
        }
    }

    public function filter(Request $request)
    {
