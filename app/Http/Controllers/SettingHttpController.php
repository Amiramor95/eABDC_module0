<?php

namespace App\Http\Controllers;

use App\Models\SettingHttp;
use GuzzleHttp\Exception\RequestException;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Http;
use Ixudra\Curl\Facades\Curl;
use Validator;

class SettingHttpController extends Controller
{
    public function get(Request $request)
    {
        try {
            $data = SettingHttp::orderBy('SETTING_HTTP_ID', 'desc')->first();

            http_response_code(200);
            return response([
                'message' => 'Data successfully retrieved.',
                'data' => $data
            ]);
        } catch (RequestException $r) {

            http_response_code(400);
            return response([
                'message' => 'Failed to retrieve data.',
                'errorCode' => 4103
            ],400);
        }
    }

    public function getAll()
    {
        try {
            $data = SettingHttp::all();

            http_response_code(200);
            return response([
                'message' => 'All data successfully retrieved.',
                'data' => $data
            ]);
        } catch (RequestException $r) {

            http_response_code(400);
            return response([
                'message' => 'Failed to retrieve all data.', 
                'errorCode' => 4103
            ],400);
        }
    }

    public function create(Request $request)
    {
        $validator = Validator::make($request->all(), [
			'API_KEY' => 'required',
			'API_SECRET' => 'required',
			'ALLOW_IP' => 'required',
        ]);

        if ($validator->fails()) {
            http_response_code(400);
            return response([
                'message' => 'Data validation error.',
                'errorCode' => 4106
            ],400);
        }

        try {
            //create function
            $data = new SettingHttp;
            $data->USER_NAME = $request->USER_NAME;
            $data->API_KEY = $request->API_KEY;
            $data->API_SECRET = $request->API_SECRET;
            $data->ALLOW_IP = $request->ALLOW_IP;
            $data->DLR_URL = $request->DLR_URL;
            $data->MO_URL  = $request->MO_URL;
            $data->save();

            http_response_code(200);
            return response([
                'message' => 'Data successfully updated.'
            ]);

        } catch (RequestException $r) {

            http_response_code(400);
            return response([
                'message' => 'Data failed to be updated.',
                'errorCode' => 4100
            ],400);
        }

    }

